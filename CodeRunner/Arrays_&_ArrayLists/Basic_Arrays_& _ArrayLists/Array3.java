
public class Array {

    public static void main(String[] args)
    {
        String[] colors = {"Green", "Blue", "Yellow"};


        System.out.println(colors[1]);



        for (int i = 0; i < colors.length; i++)
        {
            System.out.println((i + 1) + ". " + colors[i]);
        }

    }

}
